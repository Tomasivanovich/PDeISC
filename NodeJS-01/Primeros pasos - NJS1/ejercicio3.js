// Función que realiza la suma de 4 y 5
function sumar() {
    return 4 + 5; 
}

// Función que realiza la resta de 3 menos 6
function resta() {
    return 3 - 6;  
}

// Función que realiza la multiplicación de 2 por 7
function multiplicacion() {
    return 2 * 7;  
}

// Función que realiza la división de 20 entre 4
function division() {
    return 20 / 4;  
}

// Mostrar los resultados de las funciones en la consola
console.log(sumar(), resta(), multiplicacion(), division());
